//greater than operator

let val_1 = 40 > 54;
console.log(val_1) // returns false

//less than operator

let val_2 = 40 < 54;
console.log(val_2); // returns true

//greater than or equal to operator

let val_3 = 15 >= 20;
console.log(val_3); // returns false

//less than or equal to operator

let val_4 = 15 <= 20;
console.log(val_4); // returns true

//equal to operator

let val_5 = 7 == '7'; // returns true
console.log(val_5);

//strictly equal to operator

let val_6 = 7 === '7'; // returns false
console.log(val_6);

//not equal to operator

let val_7 = 7 != '7'; // returns false
console.log(val_7);

//strictly not equal to operator

let val_8 = 7 !== '7'; // returns true
console.log(val_8);

//null keyword

let myVar;
console.log(myVar); //returns undefined
console.log(typeof myVar); //returns undefined

//undefined keyword

let myVar1 = null;
console.log(myVar1); //returns null
console.log(typeof(myVar1)); //returns object

//NaN keyword

console.log(NaN == 1); // returns false because 1 is a number
